class PhysicalLayer:
    """
    [ǝkǝn -> Сформировать инвариант физического слоя]
    Описывает конкретный физический слой (например, Magnetostatics) 
    и его энергетический коэффициент (базис Eps_0 * c^k).
    """
    def __init__(self, layer_id, coeff, cells):
        self.layer_id = layer_id  # Имя слоя (например, '4_Magnetostatics')
        self.coeff = coeff        # Базисный коэффициент (например, 'Eps_0*c^2')
        self.cells = cells        # Словарь ячеек: { 'индекс_координаты': 'физ_величина' }

    def __repr__(self):
        return f"Layer({self.layer_id} -> {self.coeff}, cells_count={len(self.cells)})"


class MetaOperatorShift:
    """
    [ǝqqǝn -> Фиксация оператора пространственного сдвига/связи]
    Описывает векторные направления трансляции величин между слоями матрицы.
    """
    def __init__(self, op_id, shifts):
        self.op_id = op_id        # Имя мета-оператора (например, '30_N')
        self.shifts = shifts      # Направления переходов: { 'индекс': '^' / 'v' / '->' / '<-' }


